package com.niit.mycart.model;

public class Cart {

	public int getCartById;
	public int validate;
	public int update;
	public int getGetCartById() {
		return getCartById;
	}
	public void setGetCartById(int getCartById) {
		this.getCartById = getCartById;
	}
	public int getValidate() {
		return validate;
	}
	public void setValidate(int validate) {
		this.validate = validate;
	}
	public int getUpdate() {
		return update;
	}
	public void setUpdate(int update) {
		this.update = update;
	}
	
	
	
}